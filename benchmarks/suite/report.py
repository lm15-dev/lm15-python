"""Render benchmarks/BENCHMARKS.md (and the README Footprint section)
from RESULTS.json.  Numbers are never hand-typed: this module is the only
writer of those tables.
"""

from __future__ import annotations

import json
from pathlib import Path

BENCH_DIR = Path(__file__).resolve().parents[1]
REPO = BENCH_DIR.parent

README_START = "<!-- footprint:generated:start -->"
README_END = "<!-- footprint:generated:end -->"

ORDER = ["lm15", "openai", "anthropic", "google-genai", "litellm", "langchain-openai"]


def _fmt_ms(s: float) -> str:
    return f"{s * 1000:.0f} ms"


def _fmt_mib(mib: float) -> str:
    return f"{mib:.1f} MiB"


def _pkg_rows(res: dict) -> list[dict]:
    rows = []
    for name in ORDER:
        p = res["packages"].get(name)
        if p is None:
            continue
        rows.append({"name": name, **p})
    return rows


def footprint_table(res: dict) -> str:
    lines = [
        "| package | install size | transitive deps | cold import | import RSS |",
        "|---|---:|---:|---:|---:|",
    ]
    for row in _pkg_rows(res):
        if not row.get("ok"):
            lines.append(f"| {row['name']} | install failed | — | — | — |")
            continue
        fp, imp, rss = row["footprint"], row["import"], row["rss"]
        bold = "**" if row["name"] == "lm15" else ""
        lines.append(
            f"| {bold}{row['name']}{bold} | {_fmt_mib(fp['install_mib'])} "
            f"| {fp['transitive_deps']} | {_fmt_ms(imp['best_s'])} "
            f"| {rss['delta_kib'] / 1024:.1f} MiB |"
        )
    return "\n".join(lines)


def render_markdown(res: dict) -> str:
    env = res["environment"]
    out: list[str] = []
    a = out.append
    a("# lm15 benchmarks")
    a("")
    a("All numbers below are generated by `benchmarks/suite/run.py` and read")
    a("from `benchmarks/RESULTS.json` — nothing in this file is hand-typed.")
    a("")
    a("## Methodology")
    a("")
    a(f"- Machine: {env['cpu']} ({env['cpu_count']} CPUs), {env['os']}")
    a(f"- Python: {env['python']} · uv {env.get('uv', '?')} · run at {res['generated_at']}")
    a("- Each competitor is installed into its own scratch venv under")
    a("  `/tmp/lm15-bench-venvs/` (`uv venv` + `uv pip install`); lm15 is")
    a("  installed from the locally built wheel. Failures are recorded and the")
    a("  suite continues.")
    a("- **Cold import** = wall time of `python -c \"import X\"` in a fresh")
    a(f"  process, best of {res['config']['import_n']} (after one warmup run to")
    a("  populate the page cache and `.pyc` files). A bare `python -c pass`")
    a("  baseline is shown for scale.")
    a("- **Install size** = bytes added to `site-packages` vs an empty venv,")
    a("  measured after byte-compilation (i.e. what sits on disk after first use).")
    a("  **Transitive deps** = installed package count minus the package itself.")
    a("- **Import RSS** = median `VmRSS` after the import minus a bare")
    a("  interpreter in the same venv.")
    a(f"- **Hot path** (lm15 only) = median ns/op over {res['config']['hotpath_n']}")
    a(f"  timed iterations after {res['config']['hotpath_warmup']} warmup")
    a("  iterations, in-process. Response bodies and the SSE transcript are")
    a("  real pinned captures from `lm15-contract/bodies/`.")
    a("- **TTFR tax** = time-to-first-byte of a streaming generate request to")
    a("  local ollama, lm15's `StdlibTransport` vs raw `urllib.request`,")
    a("  identical request bytes, interleaved runs, median of N.")
    a("")
    a("## Footprint (install size · dependencies · import)")
    a("")
    a(footprint_table(res))
    a("")
    lm15_imp = res["packages"]["lm15"].get("import")
    if lm15_imp:
        a(f"Bare interpreter start (`python -c pass`): "
          f"{_fmt_ms(lm15_imp['interpreter_baseline_s'])} — subtract it from every")
        a("row to get the library's own share of the cold import.")
        a("")
    for row in _pkg_rows(res):
        if not row.get("ok"):
            a(f"> {row['name']}: install failed — {row.get('error', '?')!r}")
            a("")

    hp = res.get("hotpath")
    if hp:
        a("## Hot path (lm15 only)")
        a("")
        a("| operation | median | ops/sec |")
        a("|---|---:|---:|")
        for prov, t in hp["build_request"].items():
            a(f"| build_request ({prov}) | {t['median_ns'] / 1000:.1f} µs | {t['ops_per_sec']:,.0f} |")
        for prov, t in hp["parse_response"].items():
            a(f"| parse_response ({prov}, pinned body) | {t['median_ns'] / 1000:.1f} µs | {t['ops_per_sec']:,.0f} |")
        rt = hp["serde_roundtrip_midsize_request"]
        a(f"| Request serde round-trip ({rt['wire_bytes']} B wire) | {rt['median_ns'] / 1000:.1f} µs | {rt['ops_per_sec']:,.0f} |")
        sp = hp["stream_pipeline"]
        a(f"| stream pipeline ({sp['transcript']}, {sp['stream_events_per_pass']} events) "
          f"| {sp['median_ns_per_pass'] / 1000:.1f} µs/transcript | {sp['events_per_sec']:,.0f} events/s |")
        a("")

    tt = res.get("ttfr")
    if tt:
        a("## TTFR tax vs raw urllib (local ollama)")
        a("")
        if tt.get("skipped"):
            a(f"Skipped: {tt['reason']}.")
        else:
            a(f"Model `{tt['model']}`, median of {tt['n']} interleaved runs:")
            a("")
            a("| client | TTFB | total |")
            a("|---|---:|---:|")
            a(f"| lm15 StdlibTransport | {tt['lm15_ttfb_ms']:.1f} ms | {tt['lm15_total_ms']:.1f} ms |")
            a(f"| raw urllib.request | {tt['urllib_ttfb_ms']:.1f} ms | {tt['urllib_total_ms']:.1f} ms |")
            a("")
            a(f"lm15 transport tax over raw stdlib HTTP: **{tt['ttfb_tax_ms']:+.1f} ms** TTFB.")
            a(f"Run-to-run TTFB spread: lm15 {tt['lm15_ttfb_spread_ms']:.0f} ms, "
              f"urllib {tt['urllib_ttfb_spread_ms']:.0f} ms — the model server's")
            a("own jitter dwarfs any difference between the two clients; the tax")
            a("is indistinguishable from zero.")
        a("")

    a("## Why lm15 is small and fast")
    a("")
    a("- **Zero runtime dependencies.** The whole library is stdlib-only")
    a("  (`websockets` is one optional extra for live sessions). There is no")
    a("  pydantic, no httpx/requests, no certifi chain to install, resolve, or")
    a("  import.")
    a("- **No pydantic.** Canonical types are plain `dataclass`es with exact,")
    a("  hand-written serde — no metaclass machinery or validators executing at")
    a("  import time or per call.")
    a("- **Stdlib transport.** HTTP goes through `http.client`/`urllib`-level")
    a("  plumbing the interpreter already loaded, so the wire layer adds")
    a("  essentially nothing over raw stdlib HTTP.")
    a("")
    return "\n".join(out) + "\n"


def readme_section(res: dict) -> str:
    lines = [
        README_START,
        "## Footprint",
        "",
        "Measured by [`benchmarks/suite/run.py`](benchmarks/suite/run.py) on "
        f"Python {res['environment']['python']} — methodology and full results in "
        "[benchmarks/BENCHMARKS.md](benchmarks/BENCHMARKS.md):",
        "",
        footprint_table(res),
        README_END,
    ]
    return "\n".join(lines)


def update_readme(res: dict) -> None:
    readme = REPO / "README.md"
    text = readme.read_text()
    block = readme_section(res)
    if README_START in text:
        pre, rest = text.split(README_START, 1)
        _, post = rest.split(README_END, 1)
        text = pre + block + post
    else:
        anchor = "## Install"
        text = text.replace(anchor, block + "\n\n" + anchor, 1)
    readme.write_text(text)


def main() -> None:
    res = json.loads((BENCH_DIR / "RESULTS.json").read_text())
    (BENCH_DIR / "BENCHMARKS.md").write_text(render_markdown(res))
    update_readme(res)


if __name__ == "__main__":
    main()
